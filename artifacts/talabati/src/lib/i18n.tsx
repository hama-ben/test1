const DICTS: Record<Locale, Dict> = { ar, fr, en };

interface I18nContextValue {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string, fallback?: string) => string;
}

const I18nCtx = createContext<I18nContextValue>({
  locale: "ar",
  setLocale: () => {},
  t: (k) => k,
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    try {
      const stored = localStorage.getItem("locale");
      if (stored === "ar" || stored === "fr" || stored === "en") return stored as Locale;
    } catch { /* ignore */ }
    return "ar";
  });

  const setLocale = (l: Locale) => {
    setLocaleState(l);
    try { localStorage.setItem("locale", l); } catch { /* ignore */ }
  };

  const t = (key: string, fallback?: string): string => {
    const dict = DICTS[locale];
    if (dict[key] !== undefined) return dict[key];
    if (locale !== "ar" && DICTS.ar[key] !== undefined) return DICTS.ar[key];
    return fallback ?? key;
  };

  return (
    <I18nCtx.Provider value={{ locale, setLocale, t }}>
      {children}
    </I18nCtx.Provider>
  );
}

export function useTranslation(): I18nContextValue {
  return useContext(I18nCtx);
}
